package com.lj.gps.base;

public class TableSort {
    private String sort;
    private String column;

    public TableSort() {
    }

    public String getSort() {
        return this.sort;
    }

    public void setSort(String sort) {
        this.sort = sort;
    }

    public String getColumn() {
        return this.column;
    }

    public void setColumn(String column) {
        this.column = column;
    }
}
